package com.vlingo.android.tools.formats;

import com.vlingo.android.tools.DeviceDescriptor;
import com.vlingo.android.tools.Format;

/**
 * Format class to provide for future expansion and use of comma-separated value (CSV) formats.
 * 
 * @author J Carter
 */
public class CsvFormat extends Format
{
	static final public boolean WITH_HEADER = true;
	static final public boolean NO_HEADER = false;
	
	public CsvFormat( boolean _option )
	{
		header = _option;
	}
	
	@Override
	public String format( DeviceDescriptor _desc )
	{
		return "CsvFormat is not supported at this time";
	}

	@SuppressWarnings("unused")
	private boolean header;
}
